package epi.tp2;

public class Categorie {

	private Integer 		Id;
	private String			Nom;
	
	public Categorie() {
		
	}

	public Integer getId() {
		return Id;
	}

	public void setId(Integer id) {
		Id = id;
	}

	public String getNom() {
		return Nom;
	}

	public void setNom(String nom) {
		Nom = nom;
	}
	
	
}
